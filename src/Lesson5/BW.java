package Less1.Lesson5;

public class BW {
    public static void main(String[] args) {

        for (int i = 0; i < 8; i++) {

            if (i % 2 != 0) {

                System.out.print("B");

            }

            for (int j = 0; j < 8; j++) {

                System.out.print("WB");

            }

            System.out.println();

        }

    }
}
